    private void btn_QuenMkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_QuenMkActionPerformed
        String email = JOptionPane.showInputDialog(this, "Nhập vào Email của bạn:", "Quên Mật Khẩu", 1);
        if (email == null) {
            JOptionPane.showMessageDialog(this, "Bạn chọn thoát");
        } else if (!email.isEmpty()) {
            try {
                Class.forName(driver);
                Connection con = DriverManager.getConnection(url, user, password);
                String sql = "SELECT * FROM NHANVIEN WHERE email = ?";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, email);
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    // Tạo mật khẩu mới ngẫu nhiên
                    String newPassword = generateRandomPassword();
                    // Mã hóa mật khẩu mới
                    String hashedNewPassword = hashPassword(newPassword);
                    // Cập nhật mật khẩu mới vào cơ sở dữ liệu
                    String updateSql = "UPDATE NHANVIEN SET pasword = ? WHERE email = ?";
                    PreparedStatement updatePs = con.prepareStatement(updateSql);
                    updatePs.setString(1, hashedNewPassword);
                    updatePs.setString(2, email);
                    updatePs.executeUpdate();
                    // Gửi email chứa mật khẩu mới đến người dùng
                    String emailContent = "Mật khẩu mới của bạn là: " + newPassword;
                    EmailSender.sendEmail(email, "Khôi phục mật khẩu", emailContent);
                    JOptionPane.showMessageDialog(this, "Một email chứa mật khẩu mới đã được gửi đến địa chỉ email của bạn.");
                } else {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy email trong cơ sở dữ liệu");
                }
                con.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Email không được để trống");
        }
    }//GEN-LAST:event_btn_QuenMkActionPerformed

