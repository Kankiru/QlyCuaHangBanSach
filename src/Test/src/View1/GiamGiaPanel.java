    private void fillTable(List<KhuyenMai> all) {
        model = (DefaultTableModel) tblKM.getModel();
        model.setRowCount(0);
        for (KhuyenMai khuyenMai : all) {
            model.addRow(khuyenMai.toDataRow());
        }
    }

    private void clear() {
        txtMKM1.setText("");
       
        txtTENKM1.setText("");
        dcNBD1.setDate(null);
        dcNKT1.setDate(null);
        sldKM.setValue(0);
        rbtnCKH1.setSelected(true);
    }

    private void showData() {
        txtMKM1.setText(tblKM.getValueAt(index, 0).toString());
        txtTENKM1.setText(tblKM.getValueAt(index, 1).toString());

        String DateNBD = tblKM.getValueAt(index, 2).toString();
        String DateNKT = tblKM.getValueAt(index, 3).toString();
        Object sldValueObj = tblKM.getValueAt(index, 5);
        if (sldValueObj != null) {
            try {
                int sldValue = Integer.parseInt(sldValueObj.toString());
                sldKM.setValue(sldValue);
            } catch (NumberFormatException e) {
                e.printStackTrace();
            }
        } else {

        }
        String LoaiKM = tblKM.getValueAt(index, 4).toString();
        if (LoaiKM.equals("Đã Kích Hoạt")) {
            rbtnKH1.setSelected(true);
        } else {
            rbtnCKH1.setSelected(true);
        }
        try {
            SimpleDateFormat SDF = new SimpleDateFormat("yyyy-MM-dd", Locale.ENGLISH);
            java.util.Date NBD = SDF.parse(DateNBD);
            java.util.Date NKT = SDF.parse(DateNKT);
            dcNBD1.setDate(NBD);
            dcNKT1.setDate(NKT);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void DisplaySearchData(List<KhuyenMai> SearchKM) {
        model = (DefaultTableModel) tblKM.getModel();
        model.setRowCount(0);
        for (KhuyenMai khuyenMai : SearchKM) {
            model.addRow(new Object[]{khuyenMai.getMAKM(), khuyenMai.getTENKHUYENMAI(), khuyenMai.getNGAYBATDAU(), khuyenMai.getNGAYKETTHUC(), khuyenMai.getGIAMGIA(), khuyenMai.getTRANGTHAI()});
        }

    }

