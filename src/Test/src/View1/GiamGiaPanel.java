    private void btnTimKiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimKiemMouseClicked
        try {

            java.util.Date SearchNBDdate = dcSearchNBD.getDate();
            java.util.Date SearchNKTdate = dcSearchNKT.getDate();
            if (SearchNBDdate == null || SearchNKTdate == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng không bỏ trống");
                return;
            }
            java.sql.Date SNBD = new java.sql.Date(SearchNBDdate.getTime());
            java.sql.Date SNKT = new java.sql.Date(SearchNKTdate.getTime());

            List<KhuyenMai> km = KMSV.Search(SNBD, SNKT);

            if (km == null || km.isEmpty()) {
                model.setRowCount(0);
                JOptionPane.showMessageDialog(this, "Không tìm thấy bất kì kết quả nào từ ngày " + SNBD.toString() + " đến ngày " + SNKT.toString());
                count();
            } else {
                DisplaySearchData(km);
                JOptionPane.showMessageDialog(this, "Đã tìm thấy kết quả từ ngày " + SNBD.toString() + " đến ngày" + SNKT.toString());
                count();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnTimKiemMouseClicked

