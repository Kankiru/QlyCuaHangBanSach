    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String matKhauMoi = txtMKM.getText();
        String nhapLaiMatKhau = txtNLMKM.getText();

        // Validate dữ liệu
        if (matKhauMoi.isEmpty() || nhapLaiMatKhau.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu mới và nhập lại mật khẩu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if (!matKhauMoi.equals(nhapLaiMatKhau)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu mới không trùng khớp. Vui lòng nhập lại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            // Thực hiện hành động xác nhận khi dữ liệu hợp lệ
            // Ví dụ: gọi hàm để lưu mật khẩu mới vào cơ sở dữ liệu, đóng cửa sổ hiện tại, hiển thị thông báo thành công, v.v.
            LoginSV.resetPassword(matKhauMoi, matKhauMoi, matKhauMoi);
            JOptionPane.showMessageDialog(this, "Xác nhận thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            this.dispose(); // Đóng cửa sổ hiện tại sau khi xác nhận thành công
        }
    }//GEN-LAST:event_btnOKActionPerformed

