    private void hienThongTinLichSuGia() {
        String maSach = (String) cbxChoiceMS.getSelectedItem();
        List<LichSuGia> lichSuGiaList = LSGSV.getLichSuGiaByMaSach(maSach);

        // Xóa dữ liệu cũ trên bảng
        DefaultTableModel model = (DefaultTableModel) tblLSG.getModel();
        model.setRowCount(0);

        // Thêm dữ liệu mới từ danh sách lịch sử giá
        for (LichSuGia lichSuGia : lichSuGiaList) {
            model.addRow(new Object[]{lichSuGia.getMaSach(), lichSuGia.getNgayCapNhat(), lichSuGia.getGiaBan()});
        }

    }

    private void fillTableTheLoai(List<TheLoai> theLoai) {

        model = (DefaultTableModel) tblTheLoai.getModel();
        model.setRowCount(0);
        for (TheLoai tl : theLoai) {
            model.addRow(tl.toDataRow());
        }
    }

    private void fillTableNXB(List<NhaXuatBan> nxb) {

        model = (DefaultTableModel) tblNXB.getModel();
        model.setRowCount(0);

        for (NhaXuatBan n : nxb) {
            model.addRow(n.toDataRow());
        }

    }

    private void fillTableTacGia(List<TacGia> tacGia) {

        model = (DefaultTableModel) tblTacGia.getModel();
        model.setRowCount(0);

        for (TacGia tg : tacGia) {
            model.setRowCount(0);

            model.addRow(tg.toDataRow());
        }

    }

    private void fillTableSach(List<Main.Sach.Model.SachModel> allSach) {
        model = (DefaultTableModel) tblSach.getModel();
        model.setRowCount(0);
        for (Main.Sach.Model.SachModel sach : allSach) {
            model.addRow(sach.toDataRow());
        }
    }

    private void hienThiDanhSachSachLenBang(List<SachModel> ketQuaTimKiem) {
        DefaultTableModel model = (DefaultTableModel) tblSach.getModel();
        model.setRowCount(0); // Xóa tất cả các hàng trong bảng

        for (SachModel sach : ketQuaTimKiem) {
            Object[] row = {
                sach.getMaSach(),
                sach.getNhaXuatBan(),
                sach.getTheLoai(),
                sach.getTacGia(),
                sach.getTenSach(),
                sach.getNamXuatBan(),
                sach.getSoLuong(),
                sach.getGiaMua()
            // Thêm các cột khác nếu cần
            };
            model.addRow(row); // Thêm một hàng mới vào bảng với dữ liệu của sách
        }
    }

