    public void fillCboMKM() {
        cbxMKM.removeAllItems(); // Xóa tất cả các mục trong combobox trước khi thêm mới
        List<String> maKhuyenMaiList = SSV.getMaKhuyenMai(); // Lấy danh sách mã khuyến mãi từ service
        for (String maKhuyenMai : maKhuyenMaiList) {
            cbxMKM.addItem(maKhuyenMai); // Thêm mã khuyến mãi vào combobox
        }
    }

    private void cbx() {
        List<String> listMaSach = LSGSV.getAllMaSach();

        // Đưa danh sách mã sách vào combobox
        for (String maSach : listMaSach) {
            cbxChoiceMS.addItem(maSach);

            fillTableTheLoai(TTSV.getTheLoai());
            fillTableNXB(TTSV.getNXB());
            fillTableTacGia(TTSV.getTacGia());
        }
    }

