    private ArrayList<Voucher> getVoucherListFromTable() {
        ArrayList<Voucher> voucherList = new ArrayList<>();
        model = (DefaultTableModel) tblKM.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String maVoucher = model.getValueAt(i, 0).toString();
            String tenVoucher = model.getValueAt(i, 1).toString();
            int dieuKien = Integer.parseInt(model.getValueAt(i, 2).toString());
            int giamGia = Integer.parseInt(model.getValueAt(i, 3).toString());
            Voucher voucher = new Voucher(maVoucher, tenVoucher, dieuKien, giamGia);
            voucherList.add(voucher);
        }
        return voucherList;
    }
