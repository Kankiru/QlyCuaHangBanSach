    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String searchOption = cbxChoice.getSelectedItem().toString();
        String searchText = txtTKVC.getText();

        ArrayList<Voucher> searchResult = new ArrayList<>();

        switch (searchOption) {
            case "Mã Voucher":
                searchResult = VCSV.searchByMaVoucher(searchText);
                break;
            case "Tên Voucher":
                searchResult = VCSV.searchByTenVoucher(searchText);
                break;
            // Add more cases for other search options if needed
            default:
                break;
        }

        if (searchResult.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả phù hợp.", "Thông0 báo", JOptionPane.INFORMATION_MESSAGE);
            model.setRowCount(0);
        } else {
            fillTable(searchResult);
            updateVoucherCount();
            JOptionPane.showMessageDialog(this, "Đã tìm thấy");

        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnSapXepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSapXepActionPerformed
        String selectedOrder = cboSapXep.getSelectedItem().toString();

        // Get the list of vouchers from the table
        ArrayList<Voucher> voucherList = getVoucherListFromTable();

        // Sort the voucher list based on the selected order
        if (selectedOrder.equals("GIẢM DẦN")) {
            Collections.sort(voucherList, Comparator.comparing(Voucher::getGiamGia).reversed());
        } else if (selectedOrder.equals("TĂNG DẦN")) {
            Collections.sort(voucherList, Comparator.comparing(Voucher::getGiamGia));
        }

        // Update the table with the sorted voucher list
        fillTable(voucherList);
    }//GEN-LAST:event_btnSapXepActionPerformed

    private void fillTable(List<Voucher> voucherList) {
        model = (DefaultTableModel) tblKM.getModel();
        model.setRowCount(0);

        // Duyệt qua danh sách voucher và thêm từng dòng vào bảng
        for (Voucher voucher : voucherList) {
            // Lấy thông tin từ voucher để tạo một hàng mới

            // Thêm hàng vào bảng
            model.addRow(voucher.toDataRow());
        }
    }

