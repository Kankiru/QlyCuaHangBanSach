    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // Lấy mã voucher từ hàng được chọn trong bảng
        int rowIndex = tblKM.getSelectedRow();
        if (rowIndex == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một voucher để xóa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String maVoucher = tblKM.getValueAt(rowIndex, 0).toString();

        // Hiển thị hộp thoại xác nhận
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa voucher này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        // Xóa voucher bằng cách gọi phương thức từ VoucherService
        boolean deleted = VCSV.deleteVoucher(maVoucher);
        if (deleted) {
            JOptionPane.showMessageDialog(this, "Xóa voucher thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            updateVoucherCount();
        } else {
            JOptionPane.showMessageDialog(this, "Xóa voucher thất bại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

        // Làm mới dữ liệu trên bảng
        fillTable(VCSV.getAllVoucher());
        updateVoucherCount();
    }//GEN-LAST:event_btnXoaActionPerformed

